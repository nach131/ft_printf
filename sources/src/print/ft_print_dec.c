#include "ft_printf.h"
#include "libft.h"

int	ft_print_dec(va_list arg, const char *format, t_print *tp)
{
		int d = va_arg(arg, int);
		ft_putnbr_fd(d, 1);
		tp->len += ft_intlen(d, *format);
		return (1);
}
