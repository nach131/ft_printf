#include "ft_printf.h"
#include "libft.h"

void	ft_print_hexa(unsigned int x)
{
	char	*hexa;
	int		res[100];
	int		i;

	hexa = "0123456789abcdef";
	i = 0;
	while (x >= 16)
	{
		res[i] = hexa[x % 16];
		x = x / 16;
		i++;
	}
	res[i] = hexa[x];
	while (i >= 0)
	{
		ft_putchar_fd(res[i], 1);
		i--;
	}
}
